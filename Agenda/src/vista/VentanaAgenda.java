    public void llenarActividad(JComboBox combo) {
        controladorActividad = new ControladorActividad();
        List<Actividad> lista = controladorActividad.listar();
        combo.addItem("Seleccionar Actividad");
        for (Actividad actividad : lista) {
            combo.addItem(actividad.getNombre());
        }
    }

    public void llenarTarea(JComboBox combo) {
        controladorTarea = new ControladorTarea();
        List<Tarea> lista = controladorTarea.listar();
        combo.addItem("Seleccionar Tarea");
        for (Tarea tarea : lista) {
            combo.addItem(tarea.getNombre());
        }
    }

    public void llenarUsuario(JComboBox combo) {
        controladorUsuario = new ControladorUsuario();
        List<Usuario> lista = controladorUsuario.listar();
        combo.addItem("Seleccionar Usuario");
        for (Usuario usuario : lista) {
            combo.addItem(usuario.getNombre() + " " + usuario.getApellido());
        }
    }

