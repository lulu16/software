    public void lista() {
        model1 = new DefaultTableModel();
        model1.addColumn("ID");
        model1.addColumn("USUARIO");
        model1.addColumn("ACTIVIDAD");
        model1.addColumn("TAREA");
        listaAgenda = controladorAgenda.listar();
        for (Agenda agenda : listaAgenda) {
            Object[] datos = new Object[1000];
            datos[0] = agenda.getId();
            datos[1] = agenda.getUsuario().getNombre() + " " + agenda.getUsuario().getApellido();
            datos[2] = agenda.getActividad().getNombre();
            datos[3] = agenda.getTarea().getNombre();
            model1.addRow(datos);
        }
        tblAgenda.setModel(model1);
        tblAgenda.getColumnModel().getColumn(0).setMaxWidth(0);
        tblAgenda.getColumnModel().getColumn(0).setMinWidth(0);
        tblAgenda.getColumnModel().getColumn(0).setPreferredWidth(0);

    }

