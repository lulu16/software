    private void btnCrearActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCrearActionPerformed
        Agenda agenda = new Agenda();
        String usuario = cbxUsuario.getSelectedItem().toString();
        String[] n = usuario.split(" ");
        String nombre = n[0];
        String apellido = n[1];
        String actividad = cbxActividad.getSelectedItem().toString();
        String tarea = cbxTarea.getSelectedItem().toString();

        Actividad a = controladorActividad.buscarActividadNombre(actividad);
        Tarea t = controladorTarea.buscarTareaNombre(tarea);
        Usuario u = controladorUsuario.buscarUsuarioNombre(nombre, apellido);
        agenda.setActividad(a);
        agenda.setTarea(t);
        agenda.setUsuario(u);
        if (controladorAgenda.crearAgenda(agenda) == true) {
            JOptionPane.showMessageDialog(null, "Ingresado correctamnte");
        } else {
            JOptionPane.showMessageDialog(null, "No fue ingresado");
        }
        limpiar();
        llenarActividad(cbxActividad);
        llenarTarea(cbxTarea);
        llenarUsuario(cbxUsuario);
        lista();

    }//GEN-LAST:event_btnCrearActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        int filaSeleccionada = tblAgenda.getSelectedRow();
        String codigo = tblAgenda.getValueAt(filaSeleccionada, 0).toString();
        int id = Integer.valueOf(codigo);
        controladorAgenda.eliminarAgenda(id);
        limpiar();
        llenarActividad(cbxActividad);
        llenarTarea(cbxTarea);
        llenarUsuario(cbxUsuario);
        lista();

    }//GEN-LAST:event_btnEliminarActionPerformed

